package util;

public class Configuration {

	private String h_min;
	private String h_max;
	private String t_min;
	private String t_max;
	private String thr_irrad;
	private String time_irrad;
	private int id;
	private String name;
	
	public String getH_min() {
		return h_min;
	}
	public void setH_min(String h_min) {
		this.h_min = h_min;
	}
	public String getH_max() {
		return h_max;
	}
	public void setH_max(String h_max) {
		this.h_max = h_max;
	}
	public String getT_min() {
		return t_min;
	}
	public void setT_min(String t_min) {
		this.t_min = t_min;
	}
	public String getT_max() {
		return t_max;
	}
	public void setT_max(String t_max) {
		this.t_max = t_max;
	}
	public String getThr_irrad() {
		return thr_irrad;
	}
	public void setThr_irrad(String thr_irrad) {
		this.thr_irrad = thr_irrad;
	}
	public String getTime_irrad() {
		return time_irrad;
	}
	public void setTime_irrad(String time_irrad) {
		this.time_irrad = time_irrad;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
